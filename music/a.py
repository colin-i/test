
import readchar

while True:
	c=readchar.readchar()
	if c=='q': break
	elif c=='z': print('do')
	elif c=='x': print('re')
	elif c=='c': print('mi')
	elif c=='v': print('fa')
	elif c=='b': print('sol')
	elif c=='n': print('la')
	elif c=='m': print('si')
	elif c==',': print('doh')

